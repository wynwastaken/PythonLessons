text = input("Type your message:\n").lower()
shift = int(input("Type the shift number:\n"))


def caesar(original_text,shift_amount,type_direction):
    display_text = ""
    for each_letter in original_text:
        if each_letter in extra:
            display_text += " "
        

        elif each_letter  not in alphabet:
            display_text += "(X)"


        elif type_direction == "encode":
            index_value = alphabet.index(each_letter)
            shifted_letter = (index_value + shift_amount)%len(alphabet)#jd gini dia bakal liat lebihny berapa
            #misal z 25 kita forward 3 ,28 dibagi 6 sisa 2 itu indexny
            display_text += alphabet[shifted_letter]


        else:
            index_value = alphabet.index(each_letter)
            shifted_letter = (index_value - shift_amount)%len(alphabet)
            display_text += alphabet[shifted_letter]





    print(display_text)


